create extension if not exists pgcrypto;

-- 既に作成済みの members テーブルを安全に補完します。
create table if not exists public.members (
  id uuid primary key default gen_random_uuid(),
  email text unique not null,
  name text not null,
  is_host boolean not null default false,
  created_at timestamptz not null default now()
);

alter table public.members add column if not exists is_host boolean not null default false;
alter table public.members add column if not exists created_at timestamptz not null default now();
create unique index if not exists members_email_lower_key on public.members (lower(email));

